public class Visitante {

    public static void main(String[] args) {

        // Declaración de variables con diferentes tipos de datos primitivos
        byte edadVisitante = 25; // Edad del visitante (byte)
        short numeroDepartamento = 101; // Número del departamento (short)
        int dniVisitante = 12345678; // Número de documento (int)
        long telefonoVisitante = 3216549870L; // Teléfono (long)
        float pesoVisitante = 70.5f; // Peso del visitante (float)
        double alturaVisitante = 1.75; // Altura del visitante (double)
        char inicialNombre = 'D'; // Inicial del nombre del visitante (char)
        boolean accesoPermitido = true; // Si el acceso está permitido (boolean)

        // Imprimir la información almacenada
        System.out.println("Información del visitante:");
        System.out.println("Edad del visitante: " + edadVisitante + " años");
        System.out.println("Número de departamento: " + numeroDepartamento);
        System.out.println("DNI del visitante: " + dniVisitante);
        System.out.println("Teléfono del visitante: " + telefonoVisitante);
        System.out.println("Peso del visitante: " + pesoVisitante + " kg");
        System.out.println("Altura del visitante: " + alturaVisitante + " metros");
        System.out.println("Inicial del nombre: " + inicialNombre);
        System.out.println("Acceso permitido: " + accesoPermitido);
    }
}
